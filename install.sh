# Script to install the automatic Background changer

# Make the script execuatable
chmod +x autobg.sh

# Place a copy of the script to /usr/local/bin/ to be executed
sudo cp autobg.sh /usr/local/bin/autobg

# Finishing up
echo "Gnome Auto Background Switcher has been successfully installed.
Run 'autobg' in the terminal to begin the program!"
